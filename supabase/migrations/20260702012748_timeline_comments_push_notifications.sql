create table if not exists public.timeline_comments (
    id bigint generated by default as identity primary key,
    family_id bigint not null references public.families(id) on delete cascade,
    chat_message_id bigint not null references public.chat_messages(id) on delete cascade,
    parent_comment_id bigint references public.timeline_comments(id) on delete cascade,
    author_caregiver_id bigint not null references public.caregivers(id) on delete cascade,
    body text not null check (char_length(trim(body)) between 1 and 1000),
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now(),
    check (parent_comment_id is null or parent_comment_id <> id)
);

create table if not exists public.push_device_tokens (
    id bigint generated by default as identity primary key,
    family_id bigint not null references public.families(id) on delete cascade,
    caregiver_id bigint not null references public.caregivers(id) on delete cascade,
    expo_push_token text not null check (char_length(trim(expo_push_token)) > 0),
    platform text not null default 'unknown' check (platform in ('ios','android','web','unknown')),
    device_id text,
    app_version text,
    enabled boolean not null default true,
    last_seen_at timestamptz not null default now(),
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now(),
    unique (caregiver_id, expo_push_token)
);

create table if not exists public.push_notification_events (
    id bigint generated by default as identity primary key,
    family_id bigint not null references public.families(id) on delete cascade,
    recipient_caregiver_id bigint not null references public.caregivers(id) on delete cascade,
    actor_caregiver_id bigint references public.caregivers(id) on delete set null,
    event_type text not null check (event_type in ('TIMELINE_COMMENT','TIMELINE_REPLY')),
    title text not null,
    body text not null,
    data jsonb not null default '{}'::jsonb,
    status text not null default 'PENDING' check (status in ('PENDING','SENT','FAILED','SKIPPED')),
    attempts integer not null default 0 check (attempts >= 0),
    error_message text,
    created_at timestamptz not null default now(),
    sent_at timestamptz,
    updated_at timestamptz not null default now()
);

create index if not exists idx_timeline_comments_message_created on public.timeline_comments(chat_message_id, created_at);
create index if not exists idx_timeline_comments_family_created on public.timeline_comments(family_id, created_at desc);
create index if not exists idx_timeline_comments_parent on public.timeline_comments(parent_comment_id);
create index if not exists idx_push_device_tokens_caregiver on public.push_device_tokens(caregiver_id, enabled);
create index if not exists idx_push_notification_events_pending on public.push_notification_events(status, created_at)
    where status = 'PENDING';
create index if not exists idx_push_notification_events_family_recipient on public.push_notification_events(family_id, recipient_caregiver_id, created_at desc);

