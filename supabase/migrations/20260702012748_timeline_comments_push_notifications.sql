create or replace function public.upsert_push_device_token_checked(
    p_family_id bigint,
    p_expo_push_token text,
    p_platform text default 'unknown',
    p_device_id text default null,
    p_app_version text default null
)
returns public.push_device_tokens
language plpgsql
set search_path = public
as $$
declare
    v_current public.caregivers%rowtype;
    v_token public.push_device_tokens%rowtype;
    v_expo_push_token text := trim(coalesce(p_expo_push_token, ''));
    v_platform text := lower(trim(coalesce(p_platform, 'unknown')));
begin
    v_current := public.current_caregiver();
    if v_current.family_id <> p_family_id then raise exception 'Family access denied'; end if;
    if v_expo_push_token = '' then raise exception 'Expo push token is required'; end if;
    if v_platform not in ('ios','android','web','unknown') then v_platform := 'unknown'; end if;

    insert into public.push_device_tokens(
        family_id,
        caregiver_id,
        expo_push_token,
        platform,
        device_id,
        app_version,
        enabled,
        last_seen_at
    ) values (
        p_family_id,
        v_current.id,
        v_expo_push_token,
        v_platform,
        nullif(trim(coalesce(p_device_id, '')), ''),
        nullif(trim(coalesce(p_app_version, '')), ''),
        true,
        now()
    )
    on conflict (caregiver_id, expo_push_token) do update set
        family_id = excluded.family_id,
        platform = excluded.platform,
        device_id = excluded.device_id,
        app_version = excluded.app_version,
        enabled = true,
        last_seen_at = now(),
        updated_at = now()
    returning * into v_token;

    return v_token;
end;
$$;

create or replace function public.create_timeline_comment_checked(
    p_family_id bigint,
    p_chat_message_id bigint,
    p_parent_comment_id bigint,
    p_body text
)
returns public.timeline_comments
language plpgsql
set search_path = public
as $$
declare
    v_current public.caregivers%rowtype;
    v_message public.chat_messages%rowtype;
    v_parent public.timeline_comments%rowtype;
    v_comment public.timeline_comments%rowtype;
    v_body text := trim(coalesce(p_body, ''));
    v_event_type text := 'TIMELINE_COMMENT';
    v_title text := '새 댓글이 달렸어요';
    v_recipient_id bigint;
begin
    v_current := public.current_caregiver();
    if v_current.family_id <> p_family_id then raise exception 'Family access denied'; end if;
    if v_body = '' then raise exception 'Comment body is required'; end if;

    select * into v_message
    from public.chat_messages
    where id = p_chat_message_id
      and family_id = p_family_id
    limit 1;

    if not found then raise exception 'Timeline item was not found'; end if;

    if p_parent_comment_id is not null then
        select * into v_parent
        from public.timeline_comments
        where id = p_parent_comment_id
          and family_id = p_family_id
          and chat_message_id = p_chat_message_id
        limit 1;

        if not found then raise exception 'Parent comment was not found'; end if;
        if v_parent.parent_comment_id is not null then raise exception 'Replies can only be added to a top-level comment'; end if;

        v_event_type := 'TIMELINE_REPLY';
        v_title := '새 답글이 달렸어요';
    end if;

    insert into public.timeline_comments(family_id, chat_message_id, parent_comment_id, author_caregiver_id, body)
    values (p_family_id, p_chat_message_id, p_parent_comment_id, v_current.id, v_body)
    returning * into v_comment;

    for v_recipient_id in
        select distinct recipient_id
        from (
            values
                (v_message.sender_id),
                (case when p_parent_comment_id is not null then v_parent.author_caregiver_id else null end)
        ) as recipients(recipient_id)
        where recipient_id is not null
          and recipient_id <> v_current.id
    loop
        insert into public.push_notification_events(
            family_id,
            recipient_caregiver_id,
            actor_caregiver_id,
            event_type,
            title,
            body,
            data
        ) values (
            p_family_id,
            v_recipient_id,
            v_current.id,
            v_event_type,
            v_title,
            left(format('%s: %s', v_current.name, v_body), 180),
            jsonb_build_object(
                'chatMessageId', p_chat_message_id,
                'commentId', v_comment.id,
                'parentCommentId', p_parent_comment_id,
                'route', '/timeline'
            )
        );
    end loop;

    return v_comment;
end;
$$;

