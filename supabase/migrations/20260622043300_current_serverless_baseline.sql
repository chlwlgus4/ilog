    if p_start_at is null or p_end_at is null then raise exception 'Schedule times are required'; end if;

    v_child := public.resolve_family_child(p_family_id, p_child_id);
    insert into public.schedules(family_id, child_id, title, category, start_at, end_at, note)
    values (p_family_id, v_child.id, v_title, p_category, p_start_at, p_end_at, nullif(trim(coalesce(p_note, '')), ''))
    returning * into v_schedule;

    insert into public.chat_messages(family_id, sender_id, body, message_type)
    values (p_family_id, v_current.id, format('%s 일정을 등록했어요.', v_schedule.title), 'TEXT');

    return v_schedule;
end;
$$;

create or replace function public.create_memory_with_chat(
    p_family_id bigint,
    p_child_id bigint,
    p_title text,
    p_note text,
    p_image_url text,
    p_tag text,
    p_happened_at timestamptz
)
returns public.memory_entries
language plpgsql
set search_path = public
as $$
declare
    v_current public.caregivers%rowtype;
    v_child public.children%rowtype;
    v_memory public.memory_entries%rowtype;
    v_title text := trim(coalesce(p_title, ''));
begin
    v_current := public.current_caregiver();
    if v_current.family_id <> p_family_id then raise exception 'Family access denied'; end if;
    if v_title = '' then raise exception 'Memory title is required'; end if;
    if p_happened_at is null then raise exception 'Memory time is required'; end if;

    v_child := public.resolve_family_child(p_family_id, p_child_id);
    insert into public.memory_entries(family_id, child_id, created_by_id, title, note, image_url, tag, happened_at)
    values (p_family_id, v_child.id, v_current.id, v_title, nullif(trim(coalesce(p_note, '')), ''), nullif(trim(coalesce(p_image_url, '')), ''), nullif(trim(coalesce(p_tag, '')), ''), p_happened_at)
    returning * into v_memory;

    insert into public.chat_messages(family_id, sender_id, body, message_type)
    values (p_family_id, v_current.id, format('수첩 기록을 남겼어요: %s', v_memory.title), 'LOG_UPDATE');

    return v_memory;
end;
$$;

create or replace function public.create_chat_message_checked(
    p_family_id bigint,
    p_body text,
    p_message_type text default 'TEXT',
    p_linked_task_id bigint default null
)
returns public.chat_messages
language plpgsql
set search_path = public
as $$
declare
    v_current public.caregivers%rowtype;
    v_task public.tasks%rowtype;
    v_message public.chat_messages%rowtype;
    v_body text := trim(coalesce(p_body, ''));
    v_message_type text := coalesce(p_message_type, 'TEXT');
begin
    v_current := public.current_caregiver();
    if v_current.family_id <> p_family_id then raise exception 'Family access denied'; end if;
    if v_body = '' then raise exception 'Message body is required'; end if;
    if v_message_type not in ('TEXT', 'TASK_LINK', 'LOG_UPDATE') then raise exception 'Invalid chat message type'; end if;

    if p_linked_task_id is not null then
        select * into v_task from public.tasks where id = p_linked_task_id and family_id = p_family_id limit 1;
        if not found then raise exception 'Linked task was not found'; end if;
    end if;

    insert into public.chat_messages(family_id, sender_id, body, message_type, linked_task_id)
    values (p_family_id, v_current.id, v_body, v_message_type, p_linked_task_id)
    returning * into v_message;

    return v_message;
end;
$$;

