create or replace function public.resolve_family_child(p_family_id bigint, p_child_id bigint default null)
returns public.children
language plpgsql
stable
set search_path = public
as $$
declare
    v_child public.children%rowtype;
begin
    if p_child_id is not null then
        select * into v_child from public.children where id = p_child_id and family_id = p_family_id limit 1;
    else
        select * into v_child from public.children where family_id = p_family_id order by id asc limit 1;
    end if;

    if not found then
        raise exception 'Child profile was not found';
    end if;

    return v_child;
end;
$$;

create or replace function public.select_task_assignee(p_family_id bigint)
returns public.caregivers
language plpgsql
stable
set search_path = public
as $$
declare
    v_caregiver public.caregivers%rowtype;
begin
    select c.* into v_caregiver
    from public.caregivers c
    left join lateral (
        select count(*)::numeric as recent_completed_tasks
        from public.tasks t
        where t.assignee_id = c.id
          and t.family_id = p_family_id
          and t.status = 'DONE'
          and t.completed_at >= now() - interval '7 days'
    ) rc on true
    left join lateral (
        select count(*)::numeric as pending_tasks_today
        from public.tasks t
        where t.assignee_id = c.id
          and t.family_id = p_family_id
          and t.status <> 'DONE'
          and t.due_at between date_trunc('day', now()) and date_trunc('day', now()) + interval '1 day' - interval '1 millisecond'
    ) pt on true
    where c.family_id = p_family_id
    order by round((coalesce(rc.recent_completed_tasks, 0) * 0.45 + coalesce(pt.pending_tasks_today, 0) * 0.80 + ((10 - c.availability_score) * 0.35) + (c.fatigue_score * 0.45))::numeric, 2), coalesce(pt.pending_tasks_today, 0), c.id
    limit 1;

    if not found then
        raise exception 'Assignable caregiver was not found';
    end if;

    return v_caregiver;
end;
$$;

create or replace function public.create_task_with_chat(
    p_family_id bigint,
    p_child_id bigint,
    p_assignee_id bigint,
    p_title text,
    p_description text,
    p_due_at timestamptz,
    p_priority text default 'MEDIUM',
    p_reminder_minutes_before integer default null
)
returns public.tasks
language plpgsql
set search_path = public
as $$
declare
    v_current public.caregivers%rowtype;
    v_child public.children%rowtype;
    v_assignee public.caregivers%rowtype;
    v_task public.tasks%rowtype;
    v_title text := trim(coalesce(p_title, ''));
    v_priority text := coalesce(p_priority, 'MEDIUM');
    v_reminder_message text := '';
begin
    v_current := public.current_caregiver();
    if v_current.family_id <> p_family_id then raise exception 'Family access denied'; end if;
    if v_title = '' then raise exception 'Task title is required'; end if;
    if p_due_at is null then raise exception 'Task due time is required'; end if;
    if v_priority not in ('HIGH', 'MEDIUM', 'LOW') then raise exception 'Invalid task priority'; end if;
    if p_reminder_minutes_before is not null and (p_reminder_minutes_before < 5 or p_reminder_minutes_before > 1440) then raise exception 'Reminder minutes must be between 5 and 1440'; end if;

    v_child := public.resolve_family_child(p_family_id, p_child_id);

    if p_assignee_id is not null then
        select * into v_assignee from public.caregivers where id = p_assignee_id and family_id = p_family_id limit 1;
        if not found then raise exception 'Assignee caregiver was not found'; end if;
    else
        v_assignee := public.select_task_assignee(p_family_id);
    end if;

    insert into public.tasks(family_id, child_id, assignee_id, created_by_id, title, description, due_at, priority, status, auto_assigned, reminder_minutes_before)
    values (p_family_id, v_child.id, v_assignee.id, v_current.id, v_title, nullif(trim(coalesce(p_description, '')), ''), p_due_at, v_priority, 'PENDING', p_assignee_id is null, p_reminder_minutes_before)
    returning * into v_task;

    if v_task.reminder_minutes_before is not null then
        v_reminder_message := format(' 리마인더는 %s분 전에 알려드릴게요.', v_task.reminder_minutes_before);
    end if;

    insert into public.chat_messages(family_id, sender_id, body, message_type, linked_task_id)
    values (p_family_id, v_current.id, format('%s님 담당으로 %s 일을 등록했어요.%s', v_assignee.name, v_task.title, v_reminder_message), 'TASK_LINK', v_task.id);

    return v_task;
end;
$$;

create or replace function public.complete_task_with_chat(p_task_id bigint)
returns public.tasks
language plpgsql
set search_path = public
as $$
declare
    v_current public.caregivers%rowtype;
    v_task public.tasks%rowtype;
begin
    v_current := public.current_caregiver();
    select * into v_task from public.tasks where id = p_task_id and family_id = v_current.family_id limit 1;
    if not found then raise exception 'Task was not found'; end if;
    if v_task.status = 'DONE' then return v_task; end if;

    update public.tasks set status = 'DONE', completed_at = now(), updated_at = now()
    where id = p_task_id and family_id = v_current.family_id
    returning * into v_task;

    insert into public.chat_messages(family_id, sender_id, body, message_type, linked_task_id)
    values (v_current.family_id, v_current.id, format('%s 일을 완료했어요.', v_task.title), 'TASK_LINK', v_task.id);

    return v_task;
end;
$$;

create or replace function public.create_log_with_chat(
    p_family_id bigint,
    p_child_id bigint,
    p_type text,
    p_value text,
    p_note text,
    p_recorded_at timestamptz
)
returns public.logs
language plpgsql
set search_path = public
as $$
declare
    v_current public.caregivers%rowtype;
    v_child public.children%rowtype;
    v_log public.logs%rowtype;
    v_value text := trim(coalesce(p_value, ''));
    v_label text;
begin
    v_current := public.current_caregiver();
    if v_current.family_id <> p_family_id then raise exception 'Family access denied'; end if;
    if p_type not in ('FEEDING', 'SLEEP', 'GROWTH', 'MOMENT', 'MEDICINE', 'CHECKLIST') then raise exception 'Invalid log type'; end if;
    if v_value = '' then raise exception 'Log value is required'; end if;
    if p_recorded_at is null then raise exception 'Recorded time is required'; end if;

    v_child := public.resolve_family_child(p_family_id, p_child_id);
    insert into public.logs(family_id, child_id, caregiver_id, type, entry_value, note, recorded_at)
    values (p_family_id, v_child.id, v_current.id, p_type, v_value, nullif(trim(coalesce(p_note, '')), ''), p_recorded_at)
    returning * into v_log;

    v_label := case v_log.type when 'FEEDING' then '수유' when 'SLEEP' then '수면' when 'GROWTH' then '성장' when 'MOMENT' then '순간' when 'MEDICINE' then '복약' when 'CHECKLIST' then '체크리스트' else '생활' end;

    insert into public.chat_messages(family_id, sender_id, body, message_type)
    values (p_family_id, v_current.id, format('%s 기록을 남겼어요: %s', v_label, v_log.entry_value), 'LOG_UPDATE');

    return v_log;
end;
$$;

create or replace function public.create_schedule_with_chat(
