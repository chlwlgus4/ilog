create table if not exists public.families (
    id bigint generated by default as identity primary key,
    name text not null,
    invite_code text not null unique,
    subscription_plan text not null default 'FREE' check (subscription_plan in ('FREE', 'PREMIUM')),
    push_notifications_enabled boolean not null default true,
    chat_notifications_enabled boolean not null default true,
    morning_briefing_enabled boolean not null default true,
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now()
);

create table if not exists public.caregivers (
    id bigint generated by default as identity primary key,
    family_id bigint not null references public.families(id) on delete cascade,
    auth_user_id uuid references auth.users(id) on delete set null,
    name text not null,
    role text not null check (role in ('MOM', 'DAD', 'GUARDIAN')),
    availability_score integer not null default 7 check (availability_score between 1 and 10),
    fatigue_score integer not null default 4 check (fatigue_score between 1 and 10),
    image_url text,
    pin_hash text not null default '',
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now()
);

create table if not exists public.children (
    id bigint generated by default as identity primary key,
    family_id bigint not null references public.families(id) on delete cascade,
    name text not null,
    birth_date date not null,
    stage text not null check (stage in ('NEWBORN', 'INFANT', 'TODDLER', 'PRESCHOOL', 'EARLY_SCHOOL')),
    image_url text,
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now()
);

create table if not exists public.tasks (
    id bigint generated by default as identity primary key,
    family_id bigint not null references public.families(id) on delete cascade,
    child_id bigint references public.children(id) on delete set null,
    assignee_id bigint references public.caregivers(id) on delete set null,
    created_by_id bigint references public.caregivers(id) on delete set null,
    title text not null,
    description text,
    due_at timestamptz not null,
    priority text not null default 'MEDIUM' check (priority in ('HIGH', 'MEDIUM', 'LOW')),
    status text not null default 'PENDING' check (status in ('PENDING', 'IN_PROGRESS', 'DONE')),
    auto_assigned boolean not null default false,
    created_at timestamptz not null default now(),
    completed_at timestamptz,
    reminder_minutes_before integer check (reminder_minutes_before is null or reminder_minutes_before >= 0),
    updated_at timestamptz not null default now()
);

create table if not exists public.schedules (
