create or replace function public.care_record_label(p_type text)
returns text
language sql
stable
set search_path = public
as $$
    select case p_type
        when 'FEEDING' then '수유'
        when 'SLEEP' then '수면'
        when 'DIAPER' then '배변'
        when 'TEMPERATURE' then '체온'
        when 'MEDICINE' then '복약'
        when 'PUMPING' then '유축'
        when 'MEMO' then '메모'
        when 'MOMENT' then '순간'
        when 'GROWTH' then '성장'
        when 'CHECKLIST' then '체크리스트'
        else '생활'
    end
$$;

create or replace function public.create_care_record_with_chat(
    p_family_id bigint,
    p_child_id bigint,
    p_type text,
    p_value text,
    p_note text,
    p_recorded_at timestamptz,
    p_recorded_end_at timestamptz default null,
    p_record_subtype text default null,
    p_details jsonb default '{}'::jsonb
)
returns public.logs
language plpgsql
set search_path = public
as $$
declare
    v_current public.caregivers%rowtype;
    v_child public.children%rowtype;
    v_log public.logs%rowtype;
    v_value text := trim(coalesce(p_value, ''));
    v_details jsonb := coalesce(p_details, '{}'::jsonb);
begin
    v_current := public.current_caregiver();
    if v_current.family_id <> p_family_id then raise exception 'Family access denied'; end if;
    if p_type not in ('FEEDING','SLEEP','GROWTH','MOMENT','MEDICINE','CHECKLIST','DIAPER','TEMPERATURE','PUMPING','MEMO') then raise exception 'Invalid log type'; end if;
    if v_value = '' then raise exception 'Log value is required'; end if;
    if p_recorded_at is null then raise exception 'Recorded time is required'; end if;

    v_child := public.resolve_family_child(p_family_id, p_child_id);

    insert into public.logs(family_id, child_id, caregiver_id, type, entry_value, note, recorded_at, recorded_end_at, record_subtype, details)
    values (
        p_family_id,
        v_child.id,
        v_current.id,
        p_type,
        v_value,
        nullif(trim(coalesce(p_note, '')), ''),
        p_recorded_at,
        p_recorded_end_at,
        nullif(trim(coalesce(p_record_subtype, '')), ''),
        v_details
    )
    returning * into v_log;

    insert into public.chat_messages(family_id, sender_id, body, message_type)
    values (p_family_id, v_current.id, format('%s 기록을 남겼어요: %s', public.care_record_label(v_log.type), v_log.entry_value), 'LOG_UPDATE');

    return v_log;
end;
$$;

create or replace function public.update_care_record_checked(
    p_log_id bigint,
    p_value text,
    p_note text,
    p_recorded_at timestamptz,
    p_recorded_end_at timestamptz default null,
    p_record_subtype text default null,
    p_details jsonb default '{}'::jsonb
)
returns public.logs
language plpgsql
set search_path = public
as $$
declare
    v_current public.caregivers%rowtype;
    v_log public.logs%rowtype;
    v_value text := trim(coalesce(p_value, ''));
begin
    v_current := public.current_caregiver();
    if v_value = '' then raise exception 'Log value is required'; end if;
    if p_recorded_at is null then raise exception 'Recorded time is required'; end if;

    update public.logs
    set entry_value = v_value,
        note = nullif(trim(coalesce(p_note, '')), ''),
        recorded_at = p_recorded_at,
        recorded_end_at = p_recorded_end_at,
        record_subtype = nullif(trim(coalesce(p_record_subtype, '')), ''),
        details = coalesce(p_details, '{}'::jsonb),
        updated_at = now()
    where id = p_log_id
      and family_id = v_current.family_id
      and caregiver_id = v_current.id
    returning * into v_log;

    if not found then raise exception 'Care record was not found'; end if;
    return v_log;
end;
$$;

create or replace function public.delete_care_record_checked(p_log_id bigint)
returns bigint
language plpgsql
set search_path = public
as $$
declare
    v_current public.caregivers%rowtype;
begin
    v_current := public.current_caregiver();

    delete from public.logs
    where id = p_log_id
      and family_id = v_current.family_id
      and caregiver_id = v_current.id;

    if not found then raise exception 'Care record was not found'; end if;
    return p_log_id;
end;
$$;

