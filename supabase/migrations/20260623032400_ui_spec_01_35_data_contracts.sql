create or replace function public.upsert_notification_preferences_checked(
    p_family_id bigint,
    p_feeding_enabled boolean,
    p_medicine_enabled boolean,
    p_vaccination_enabled boolean,
    p_growth_enabled boolean,
    p_briefing_enabled boolean,
    p_health_alerts_enabled boolean,
    p_daily_time time
)
returns public.notification_preferences
language plpgsql
set search_path = public
as $$
declare
    v_current public.caregivers%rowtype;
    v_preferences public.notification_preferences%rowtype;
begin
    v_current := public.current_caregiver();
    if v_current.family_id <> p_family_id then raise exception 'Family access denied'; end if;

    insert into public.notification_preferences(
        family_id,
        feeding_enabled,
        medicine_enabled,
        vaccination_enabled,
        growth_enabled,
        briefing_enabled,
        health_alerts_enabled,
        daily_time,
        updated_by_id
    ) values (
        p_family_id,
        coalesce(p_feeding_enabled, true),
        coalesce(p_medicine_enabled, true),
        coalesce(p_vaccination_enabled, true),
        coalesce(p_growth_enabled, false),
        coalesce(p_briefing_enabled, true),
        coalesce(p_health_alerts_enabled, true),
        coalesce(p_daily_time, time '08:00'),
        v_current.id
    )
    on conflict (family_id) do update set
        feeding_enabled = excluded.feeding_enabled,
        medicine_enabled = excluded.medicine_enabled,
        vaccination_enabled = excluded.vaccination_enabled,
        growth_enabled = excluded.growth_enabled,
        briefing_enabled = excluded.briefing_enabled,
        health_alerts_enabled = excluded.health_alerts_enabled,
        daily_time = excluded.daily_time,
        updated_by_id = excluded.updated_by_id,
        updated_at = now()
    returning * into v_preferences;

    return v_preferences;
end;
$$;

